
package Object_Referance;


public class MyPoint {
    private int x;
    private int y;

    public MyPoint(int x, int y){
    
    this.x=x;
    this.y=y;
    }

   
   

    public int getX(){
    
    return this.x;
    }
    
    
      public int getY(){
    
    return this.y;
    }
      
      
      public void setX(int x){
      
      this.x=x;
      }
      
      
      
        
      public void setY(int y){
      
      this.x=y;
      }
      
      
    
    
}
